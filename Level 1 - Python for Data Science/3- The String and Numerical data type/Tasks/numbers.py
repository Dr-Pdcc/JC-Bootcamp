num_1 = int(input("Enter the first integer:"))
num_2 = int(input("Enter the second integer:"))
num_3 = int(input("Enter the third integer:"))

sum_numbers = num_1 + num_2 + num_3
print(f"The sum of all the numbers is:{sum_numbers}")
difference = num_1 - num_2
print(f"The first number minus the second number is:{difference}")
multiplication = num_3 * num_1
print(f"The third number multiplied by the first number is:{multiplication}")
sum_over_third = (num_1 + num_2 + num_3) / num_3
print(f"The sum of all three numbers divided by the third number is:{sum_over_third}" )